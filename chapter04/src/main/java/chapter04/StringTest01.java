package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\temp");
		System.out.println("c:\\temp");
		System.out.println("\"HELLO\"");
		// \t : tab
		// \r : carriage return
		// \n : new line
		// \b : bell
		
		System.out.println("1"+"\r\n");
		System.out.println("2");
		System.out.println("hello\tworld\n");
	}
}
